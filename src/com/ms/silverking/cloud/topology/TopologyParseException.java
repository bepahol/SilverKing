package com.ms.silverking.cloud.topology;

import java.io.IOException;

public class TopologyParseException extends IOException {
	public TopologyParseException() {
	}

	public TopologyParseException(String arg0) {
		super(arg0);
	}

	public TopologyParseException(Throwable arg0) {
		super(arg0);
	}

	public TopologyParseException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
